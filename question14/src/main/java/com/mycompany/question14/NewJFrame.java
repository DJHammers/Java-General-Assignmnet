    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
        double fahrenheit = Double.parseDouble(textField1.getText());
        double result = 0;

        if (jRadioButton1.isSelected()) {
            result = (fahrenheit - 32) * 5 / 9;
            jLabel3.setText(fahrenheit + " Fahrenheit = " + result + " Celsius");
        } else if (jRadioButton2.isSelected()) {
            result = (fahrenheit - 32) * 5 / 9 + 273.15;
            jLabel3.setText(fahrenheit + " Fahrenheit = " + result + " Kelvin");
        } else {
            jLabel3.setText("Please select a conversion option.");
        }
    } catch (NumberFormatException ex) {
        jLabel3.setText("Invalid input! Please enter a valid number.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

